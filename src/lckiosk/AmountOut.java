    private void proceedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedButtonActionPerformed
        var amountText = AmountField.getText();
        double amount = Double.parseDouble(acc.getAccount(2));
        double takeAmount = Double.parseDouble(amountText);
        
        try {
            if(takeAmount > amount){
                JOptionPane.showMessageDialog(null, "Invalid Amount");
            }else if (amount >= 0) {
                double total = amount - takeAmount;
                acc.updateBalance(total);
                JOptionPane.showMessageDialog(null, "Cash out successfully.");
                Receipt rt = new Receipt(acc);
                rt.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Amount");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Amount. Please enter a numeric value.");
        }
    }//GEN-LAST:event_proceedButtonActionPerformed

